#'
#' @note this example is taken from the roxygen documnetation here: https://cran.r-project.org/web/packages/roxygen2/vignettes/roxygen2.html
#'
#' @export
add = function(x, y){
  # don't use the 'ticked' comments in here!
  x + y
}














